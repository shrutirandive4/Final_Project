    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        MainJFrame mainPage = new MainJFrame();
        
        String username = txtUsername.getText();
        String password = jPasswordField.getText();
        
        //System.out.println("====== LOGIN PAGE =========="+username+"===="+password);
        if (txtUsername.getText().isBlank()&&
            jPasswordField.getText().isBlank() )
        {
            JOptionPane.showMessageDialog(null, "Please enter data in all the fields.");
            return;
        }
        
        
        SystemLoginQuery In= new SystemLoginQuery();
        boolean res=In.CheckUsernamePwd(username,password);
        if(res==true)
        {
            dispose();
            if(In.getRole(username).equalsIgnoreCase("Owner"))
            {
            OwnerJFrame owner = new OwnerJFrame();
            owner.setVisible(true);
            return;
            }
            if(In.getRole(username).equalsIgnoreCase("Guest"))
            {
            mainPage.setUsernameEmail(username);
            GuestJFrame owner = new GuestJFrame();
            owner.setVisible(true);
            return;
            }
            if(In.getRole(username).equalsIgnoreCase("Manager"))
            {
            ManagerJFrame owner = new ManagerJFrame();
            owner.setVisible(true);
            return;
            }
            if(In.getRole(username).equalsIgnoreCase("Receptionist"))
            {
            ReceptionistJFrame receptionist = new ReceptionistJFrame();
            receptionist.setVisible(true);
            return;
            }
            if(In.getRole(username).equalsIgnoreCase("Accountant"))
            {
            AccountantJFrame accountant = new AccountantJFrame();
            accountant.setVisible(true);
            return;
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please enter valid data.");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

